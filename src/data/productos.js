export const PRECIO_BASE = 40000;
export const PRECIO_ALTA = 60000;

export function calcularPrecio(tipoSuela) {
  return tipoSuela === 'alta' ? PRECIO_ALTA : PRECIO_BASE;
}

export function formatearPrecio(precio) {
  return new Intl.NumberFormat('es-CO', {
    style: 'currency',
    currency: 'COP',
    minimumFractionDigits: 0,
  }).format(precio);
}

